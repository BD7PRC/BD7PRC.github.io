// Helper function to get country from callsign
export function getCountryFromCallsign(callsign: string): { country: string; continent: string } | null {
  if (!callsign || callsign.length < 2) return null
  
  callsign = callsign.toUpperCase()
  
  // Try full 2-letter + digit prefix first (e.g., "BA7", "BD7")
  const prefix2 = callsign.substring(0, 2)
  if (ituPrefixMap[prefix2]) {
    return ituPrefixMap[prefix2]
  }
  
  // Try single letter prefix (e.g., "B", "K")
  const prefix1 = callsign.substring(0, 1)
  if (ituPrefixMap[prefix1]) {
    return ituPrefixMap[prefix1]
  }
  
  return null
}

// Extract DXCC prefix from callsign
export function getDXCCPrefix(callsign: string): string {
  if (!callsign) return 'Unknown'
  
  const match = callsign.match(/^[A-Z]{1,2}[0-9]/i)
  if (match) {
    return match[0].toUpperCase()
  }
  
  return callsign.substring(0, 3).toUpperCase()
}
